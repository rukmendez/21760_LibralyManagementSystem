public void populate_table(){
    sf=HibernateUtil.getSessionFactory();
     Session ses=sf.openSession();
            DefaultTableModel tb=new DefaultTableModel();
            tb.addColumn("client_no");
            tb.addColumn("First_name");
            tb.addColumn("Last_name");
            tb.addColumn("phone_number");
            tb.addColumn("client_email_address");
            tb.addColumn("Image");
            tb.addColumn("Client_Category");
            tb.addColumn("DateOf_return");
            tb.addColumn("Book_Category");
            String Hql="from ClientModel";
            Query q=ses.createQuery(Hql);
            List<ClientModel> Collection=q.list();
            for(ClientModel cl : Collection)
            {
            tb.addRow(new Object[]{cl.getId(),cl.getFname(),cl.getLname(),cl.getPhone(),cl.getEmail(),cl.getPhoto(),cl.getCategory(),cl.getDate(),cl.getBookcategory()});
            this.tblDisplay.setModel(tb);
            }
}

