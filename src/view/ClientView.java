    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        FileNameExtensionFilter fenf = new FileNameExtensionFilter("IMAGES", "png", "jpeg", "jpg");
        file.addChoosableFileFilter(fenf);
        if (file.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            File filec = file.getSelectedFile();
            FilePath = filec.getAbsolutePath();
            img = new ImageIcon(FilePath);
            Image image = img.getImage().getScaledInstance(lblPhoto.getWidth(), lblPhoto.getHeight(), Image.SCALE_SMOOTH);
            lblPhoto.setIcon(new ImageIcon(image));
        }
    }//GEN-LAST:event_btnUploadActionPerformed

